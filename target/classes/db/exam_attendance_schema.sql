-- Schema for one-time exam enforcement and marks storage
-- Run this in Oracle FREEPDB1 as SYSTEM user AFTER running schema.sql, questions_schema.sql, exam_answers_schema.sql

-- 1. Create EXAM_ATTEMPTS table to enforce one-time exam per skill per student
CREATE TABLE exam_attempts (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES users(id),
    skill_id NUMBER NOT NULL REFERENCES skills(id),
    score NUMBER DEFAULT 0,
    total_questions NUMBER DEFAULT 0,
    percentage NUMBER DEFAULT 0,
    attempted_at TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT uq_user_skill_attempt UNIQUE (user_id, skill_id)
);

-- 2. Add score and total_questions columns to assessment table (if not already present)
-- NOTE: If columns already exist, these will fail — that's OK, just skip the error.
ALTER TABLE assessment ADD score NUMBER DEFAULT 0;
ALTER TABLE assessment ADD total_questions NUMBER DEFAULT 0;

COMMIT;
