-- Oracle SQL Script for Skill Gap Analysis System
-- Run this script in SQL*Plus or SQL Developer to create tables and sample data

-- Create USERS table
CREATE TABLE users (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(100) UNIQUE NOT NULL,
    password VARCHAR2(255) NOT NULL,
    role VARCHAR2(50) NOT NULL,
    full_name VARCHAR2(200),
    department VARCHAR2(50),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- Create SKILLS table
CREATE TABLE skills (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    skill_name VARCHAR2(100) NOT NULL,
    category VARCHAR2(100) NOT NULL,
    description VARCHAR2(500)
);

-- Create ASSESSMENT table
CREATE TABLE assessment (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    skill_id NUMBER NOT NULL REFERENCES skills(id),
    required_level NUMBER(1) NOT NULL,
    actual_level NUMBER(1) NOT NULL,
    gap_level NUMBER(1),
    assessed_at TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- Create RESOURCES table
CREATE TABLE resources (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    skill_id NUMBER NOT NULL REFERENCES skills(id),
    title VARCHAR2(200) NOT NULL,
    resource_url VARCHAR2(500) NOT NULL,
    resource_type VARCHAR2(50)
);

